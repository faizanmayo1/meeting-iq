export type DriverStatus = "on_duty" | "off_duty" | "hos_warning" | "hos_critical";

export type Driver = {
  id: string;
  name: string;
  unit: string;
  status: DriverStatus;
  score: number;          // 0–5 performance
  otdPct: number;         // on-time delivery %
  milesYtd: number;
  hoursLeft: string;      // HOS clock readable
  hoursLeftMins: number;  // for sorting / severity
  tenureYrs: number;
  region: "Southeast" | "West" | "South" | "Northeast" | "Midwest" | "Mountain";
  currentLoad?: string;
  trend: number[];        // last 12 weeks score
  highlight?: string;     // AI note ("3 perfect deliveries this week")
  flag?: { tone: "danger" | "warning" | "success" | "accent"; label: string };
};

const trend = (start: number, drift: number, jitter: number, n = 12) =>
  Array.from({ length: n }, (_, i) => {
    const t = i / (n - 1);
    const noise = (Math.sin(i * 1.6) + Math.cos(i * 2.4)) * jitter;
    return start + drift * t + noise;
  });

